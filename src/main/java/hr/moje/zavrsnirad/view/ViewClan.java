    private void ucitaj() {
        DefaultListModel<Clan> m = new DefaultListModel<>();
        obrada.getPodaci().forEach(s -> m.addElement(s));
        lstPodaci.setModel(m);
    }
    
    
    private void ucitajVrijednosti(){
        obrada.getEntitet().setIme(txtIme.getText());
        obrada.getEntitet().setPrezime(txtPrezime.getText());
        obrada.getEntitet().setEmail(txtEmail.getText());
        obrada.getEntitet().setUlica(txtUlica.getText());
        obrada.getEntitet().setKucni_broj(txtKucniBroj.getText());
        obrada.getEntitet().setGrad(txtGrad.getText());
        obrada.getEntitet().setDrzava(txtDrzava.getText());
                
    }
    
    private void postaviVrijednosti(){
        txtIme.setText(obrada.getEntitet().getIme());
        txtPrezime.setText(obrada.getEntitet().getPrezime());
        txtEmail.setText(obrada.getEntitet().getEmail());
        txtUlica.setText(obrada.getEntitet().getUlica());
        txtKucniBroj.setText(obrada.getEntitet().getKucni_broj());
        txtGrad.setText(obrada.getEntitet().getGrad());
        txtDrzava.setText(obrada.getEntitet().getDrzava());
        
        
        
    }
